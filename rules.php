Reglas de Memedone</span>
</span>
</h1>
<h2 >
<span >
<span >
Algunas reglas para mantener Memedone seguro y divertido para todos</span>
</span>
</h2>
<p class="" >
<span >
<span >
Memedone tiene que ver con la diversión. </span>
<span >
Es un lugar que le da a la gente el poder de hacer más feliz al mundo.</span>
</span>
</p>
<p class="" >
<span >
<span >
Para mantener en marcha la genialidad de nuestra comunidad, trata a tus compañeros de Memedone como te gustaría que te trataran: con respeto. </span>
<span >
Confiamos en que usted sea responsable, y millones de usuarios respetan esa confianza. </span>
<span >
Aquí están las reglas básicas desarrolladas de acuerdo con este principio. </span>
<span >
Tómelos en serio y respete el espíritu con el que fueron creados.</span>
</span>
</p>
<h3 >
<span >
<span >
No pornografía</span>
</span>
</h3>
<p class="" >
<span >
<span >
Memedone no es para pornografía o contenido sexualmente explícito. </span>
<span >
Trabajamos en estrecha colaboración con las fuerzas del orden y denunciamos la explotación infantil.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin violencia, contenido sangriento y dañino.</span>
</span>
</h3>
<p class="" >
<span >
<span >
No publique ni comente con contenido violento y sangriento o contenido que aliente a otros a hacer cosas que puedan hacer que se lastimen o cometan actos específicos de violencia, especialmente niños. </span>
<span >
Si publica contenido gráfico en un contexto de noticias o documental, tenga en cuenta proporcionar información suficiente para ayudar a las personas a comprender lo que está sucediendo en el contenido.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin odio ni intimidación</span>
</span>
</h3>
<p class="" >
<span >
<span >
No apoyamos contenido y comentarios que promuevan o toleren la violencia contra individuos o grupos por motivos de raza u origen étnico, religión, discapacidad, género, edad, nacionalidad, condición de veterano u orientación sexual, o cuyo propósito principal sea incitar al odio sobre la base de estas características centrales.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin spam, manipulación y abuso de múltiples cuentas</span>
</span>
</h3>
<p class="" >
<span >
<span >
Todos odian el spam. </span>
<span >
No publique grandes cantidades de contenido y comentarios no dirigidos, no deseados o repetitivos para aumentar las vistas. </span>
<span >
No se permite crear varias cuentas con usos superpuestos o para evadir la prohibición temporal o permanente de una cuenta separada.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin contenido engañoso</span>
</span>
</h3>
<p class="" >
<span >
<span >
No difunda información incorrecta. </span>
<span >
El contenido engañoso, el comentario y la cuenta pueden eliminarse dependiendo de su gravedad.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin información personal y confidencial.</span>
</span>
</h3>
<p class="" >
<span >
<span >
Para proteger la seguridad de las personas privadas, no se permite nada que revele información de identificación personal de otras personas (incluidas las cuentas personales de redes sociales de figuras no públicas sin el consentimiento de la parte original).</span>
</span>
</p>
<h3 >
<span >
<span >
No hay actividades ilegales.</span>
</span>
</h3>
<p class="" >
<span >
<span >
El contenido que representa o aboga por actividades ilegales no será tolerado. </span>
<span >
Tales actos pueden ser reportados a la policía y los usuarios que cometieron estos actos pueden ser permanentemente prohibidos de Memedone.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin suplantación</span>
</span>
</h3>
<p class="" >
<span >
<span >
No puede hacerse pasar por otros (persona, grupo o marca) en Memedone de una manera que pretende o engaña, confunde o engaña a otros.</span>
</span>
</p>
<h3 >
<span >
<span >
Sin infracción de derechos de autor y marca registrada</span>
</span>
</h3>
<p class="" >
<span >
<span >
Respeta la originalidad y la creatividad. </span>
<span >
Dar crédito a quien crédito merece. </span>
<span >
Nos reservamos el derecho de reclamar nombres de usuario en nombre de empresas o individuos que tienen reclamos legales o marcas registradas en esos nombres de usuario. </span>
